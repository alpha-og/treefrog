import { useState } from 'react'
import { useAuth } from '../lib/auth'
import { useNavigate, useSearch } from '@tanstack/react-router'
import { motion } from 'motion/react'
import { FileText, Loader2, Eye, EyeOff, Mail, Lock, ArrowLeft, User } from 'lucide-react'
import { easeOutExpo, ANIMATION_DURATIONS } from '../lib/animations'
import { Link } from '@tanstack/react-router'
import type { Provider } from '@supabase/supabase-js'

interface AuthPageProps {
  mode: 'sign-in' | 'sign-up' | 'reset-password'
}

function isValidRedirect(redirect: string): boolean {
  if (!redirect) return false
  if (redirect.startsWith('/') && !redirect.startsWith('//')) return true
  try {
    const url = new URL(redirect, window.location.origin)
    return url.origin === window.location.origin
  } catch {
    return false
  }
}

export default function AuthPage({ mode }: AuthPageProps) {
  const { signIn, signUp, resetPassword, signInWithOAuth } = useAuth()
  const navigate = useNavigate()
  const search = useSearch({ strict: false })
  const rawRedirect = (search as { redirect?: string })?.redirect || '/dashboard'
  const redirectUrl = isValidRedirect(rawRedirect) ? rawRedirect : '/dashboard'
  
  const [email, setEmail] = useState('')
  const [password, setPassword] = useState('')
  const [name, setName] = useState('')
  const [showPassword, setShowPassword] = useState(false)
  const [error, setError] = useState<string | null>(null)
  const [success, setSuccess] = useState<string | null>(null)
  const [isLoading, setIsLoading] = useState(false)

  const handleOAuthSignIn = async (provider: Provider) => {
    setError(null)
    setIsLoading(true)
    const { error } = await signInWithOAuth(provider)
    if (error) {
      setError(error.message)
      setIsLoading(false)
    }
  }

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault()
    setError(null)
    setSuccess(null)
    setIsLoading(true)

    try {
      if (mode === 'reset-password') {
        const { error } = await resetPassword(email)
        if (error) {
          setError(error.message)
        } else {
          setSuccess('Password reset email sent! Check your inbox.')
        }
      } else if (mode === 'sign-up') {
        const { error, needsConfirmation } = await signUp(email, password, {
          data: { full_name: name }
        })
        if (error) {
          setError(error.message)
        } else if (needsConfirmation) {
          setSuccess('Account created! Please check your email to confirm your account.')
        } else {
          navigate({ to: redirectUrl as never })
        }
      } else {
        const { error } = await signIn(email, password)
        if (error) {
          setError(error.message)
        } else {
          navigate({ to: redirectUrl as never })
        }
      }
    } catch {
      setError('An unexpected error occurred')
    } finally {
      setIsLoading(false)
    }
  }

  return (
    <div className="min-h-screen bg-background flex relative">
      <Link
        to="/"
        className="absolute top-4 left-4 z-10 text-sm text-muted-foreground hover:text-foreground transition-colors inline-flex items-center gap-1"
      >
        <ArrowLeft className="w-4 h-4" />
        Back to home
      </Link>
      <div className="hidden lg:flex lg:w-1/2 bg-gradient-to-br from-primary/10 via-primary/5 to-background items-center justify-center p-12">
        <motion.div
          initial={{ opacity: 0, y: 20 }}
          animate={{ opacity: 1, y: 0 }}
          transition={{
            duration: ANIMATION_DURATIONS.normal,
            ease: easeOutExpo,
          }}
          className="max-w-md"
        >
          <div className="flex items-center gap-3 mb-8">
            <img
              src="/appicon.png"
              alt="TreeFrog"
              className="w-12 h-12 rounded-xl"
            />
            <span className="text-2xl font-bold text-foreground">TreeFrog</span>
          </div>

          <h1 className="text-4xl font-bold text-foreground mb-4">
            LaTeX Compilation,
            <br />
            Simplified
          </h1>
          <p className="text-lg text-muted-foreground mb-8">
            Build LaTeX documents locally or in the cloud. Fast, reliable, and
            beautiful PDFs every time.
          </p>

          <div className="space-y-4">
            <div className="flex items-center gap-3 text-muted-foreground">
              <div className="w-8 h-8 rounded-lg bg-primary/10 flex items-center justify-center">
                <FileText className="w-4 h-4 text-primary" />
              </div>
              <span>50+ builds per month free</span>
            </div>
            <div className="flex items-center gap-3 text-muted-foreground">
              <div className="w-8 h-8 rounded-lg bg-primary/10 flex items-center justify-center">
                <FileText className="w-4 h-4 text-primary" />
              </div>
              <span>Local Docker compilation</span>
            </div>
            <div className="flex items-center gap-3 text-muted-foreground">
              <div className="w-8 h-8 rounded-lg bg-primary/10 flex items-center justify-center">
                <FileText className="w-4 h-4 text-primary" />
              </div>
              <span>Live PDF preview</span>
            </div>
          </div>
        </motion.div>
      </div>

      <div className="flex-1 flex items-center justify-center p-8 bg-background">
        <motion.div
          initial={{ opacity: 0, scale: 0.95 }}
          animate={{ opacity: 1, scale: 1 }}
          transition={{
            duration: ANIMATION_DURATIONS.normal,
            ease: easeOutExpo,
          }}
          className="w-full max-w-md"
        >
          <div className="lg:hidden flex items-center gap-3 mb-8 justify-center">
            <img
              src="/appicon.png"
              alt="TreeFrog"
              className="w-10 h-10 rounded-xl"
            />
            <span className="text-xl font-bold text-foreground">TreeFrog</span>
          </div>

          <div className="bg-card border border-border rounded-2xl shadow-xl p-8">
            <div className="mb-6 text-center">
              <h2 className="text-2xl font-bold text-foreground mb-1">
                {mode === 'reset-password' ? 'Reset Password' : mode === 'sign-in' ? 'Welcome back' : 'Create your account'}
              </h2>
              <p className="text-muted-foreground text-sm">
                {mode === 'reset-password' 
                  ? 'Enter your email to receive a password reset link' 
                  : mode === 'sign-in' 
                    ? 'Sign in to your account to continue' 
                    : 'Get started with TreeFrog today'}
              </p>
            </div>

            {error && (
              <div className="mb-4 p-3 rounded-lg bg-red-500/10 border border-red-500/20 text-red-500 text-sm">
                {error}
              </div>
            )}

            {success && (
              <div className="mb-4 p-3 rounded-lg bg-green-500/10 border border-green-500/20 text-green-500 text-sm">
                {success}
              </div>
            )}

            {mode !== 'reset-password' && (
              <div className="space-y-3 mb-6">
                <button
                  type="button"
                  onClick={() => handleOAuthSignIn('google')}
                  disabled={isLoading}
                  className="w-full py-3 rounded-xl border border-border bg-card text-foreground font-medium hover:bg-accent transition-colors text-sm flex items-center justify-center gap-2 disabled:opacity-50"
                >
                  <svg className="w-5 h-5" viewBox="0 0 24 24">
                    <path fill="#4285F4" d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"/>
                    <path fill="#34A853" d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"/>
                    <path fill="#FBBC05" d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"/>
                    <path fill="#EA4335" d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"/>
                  </svg>
                  Continue with Google
                </button>
                <button
                  type="button"
                  onClick={() => handleOAuthSignIn('github')}
                  disabled={isLoading}
                  className="w-full py-3 rounded-xl border border-border bg-card text-foreground font-medium hover:bg-accent transition-colors text-sm flex items-center justify-center gap-2 disabled:opacity-50"
                >
                  <svg className="w-5 h-5" fill="currentColor" viewBox="0 0 24 24">
                    <path d="M12 0C5.37 0 0 5.37 0 12c0 5.31 3.435 9.795 8.205 11.385.6.105.825-.255.825-.57 0-.285-.015-1.23-.015-2.235-3.015.555-3.795-.735-4.035-1.41-.135-.345-.72-1.41-1.23-1.695-.42-.225-1.02-.78-.015-.795.945-.015 1.62.87 1.845 1.23 1.08 1.815 2.805 1.305 3.495.99.105-.78.42-1.305.765-1.605-2.67-.3-5.46-1.335-5.46-5.925 0-1.305.465-2.385 1.23-3.225-.12-.3-.54-1.53.12-3.18 0 0 1.005-.315 3.3 1.23.96-.27 1.98-.405 3-.405s2.04.135 3 .405c2.295-1.56 3.3-1.23 3.3-1.23.66 1.65.24 2.88.12 3.18.765.84 1.23 1.905 1.23 3.225 0 4.605-2.805 5.625-5.475 5.925.435.375.81 1.095.81 2.22 0 1.605-.015 2.895-.015 3.3 0 .315.225.69.825.57A12.02 12.02 0 0024 12c0-6.63-5.37-12-12-12z"/>
                  </svg>
                  Continue with GitHub
                </button>
              </div>
            )}

            {mode !== 'reset-password' && (
              <div className="relative mb-6">
                <div className="absolute inset-0 flex items-center">
                  <div className="w-full border-t border-border"></div>
                </div>
                <div className="relative flex justify-center text-sm">
                  <span className="px-2 bg-card text-muted-foreground">or continue with email</span>
                </div>
              </div>
            )}

            <form onSubmit={handleSubmit} className="space-y-4">
              {mode === 'sign-up' && (
                <div>
                  <label className="block text-sm font-medium text-foreground mb-2">
                    Full Name
                  </label>
                  <div className="relative">
                    <input
                      type="text"
                      value={name}
                      onChange={(e) => setName(e.target.value)}
                      className="w-full px-4 py-3 pl-10 rounded-xl border border-border bg-background text-foreground placeholder:text-muted-foreground focus:outline-none focus:ring-2 focus:ring-primary/50 focus:border-primary transition-all text-sm"
                      placeholder="John Doe"
                      required
                    />
                    <User className="absolute left-3 top-1/2 -translate-y-1/2 w-4 h-4 text-muted-foreground" />
                  </div>
                </div>
              )}

              <div>
                <label className="block text-sm font-medium text-foreground mb-2">
                  Email
                </label>
                <div className="relative">
                  <input
                    type="email"
                    value={email}
                    onChange={(e) => setEmail(e.target.value)}
                    className="w-full px-4 py-3 pl-10 rounded-xl border border-border bg-background text-foreground placeholder:text-muted-foreground focus:outline-none focus:ring-2 focus:ring-primary/50 focus:border-primary transition-all text-sm"
                    placeholder="you@example.com"
                    required
                  />
                  <Mail className="absolute left-3 top-1/2 -translate-y-1/2 w-4 h-4 text-muted-foreground" />
                </div>
              </div>

              {mode !== 'reset-password' && (
                <div>
                  <label className="block text-sm font-medium text-foreground mb-2">
                    Password
                  </label>
                  <div className="relative">
                    <input
                      type={showPassword ? 'text' : 'password'}
                      value={password}
                      onChange={(e) => setPassword(e.target.value)}
                      className="w-full px-4 py-3 pl-10 pr-10 rounded-xl border border-border bg-background text-foreground placeholder:text-muted-foreground focus:outline-none focus:ring-2 focus:ring-primary/50 focus:border-primary transition-all text-sm"
                      placeholder="••••••••"
                      required
                      minLength={6}
                    />
                    <Lock className="absolute left-3 top-1/2 -translate-y-1/2 w-4 h-4 text-muted-foreground" />
                    <button
                      type="button"
                      onClick={() => setShowPassword(!showPassword)}
                      className="absolute right-3 top-1/2 -translate-y-1/2 text-muted-foreground hover:text-foreground transition-colors"
                    >
                      {showPassword ? <EyeOff className="w-4 h-4" /> : <Eye className="w-4 h-4" />}
                    </button>
                  </div>
                </div>
              )}

              <button
                type="submit"
                disabled={isLoading}
                className="w-full py-3 rounded-xl bg-primary text-primary-foreground font-semibold hover:opacity-90 transition-opacity text-sm disabled:opacity-50 disabled:cursor-not-allowed flex items-center justify-center gap-2"
              >
                {isLoading ? (
                  <Loader2 className="w-4 h-4 animate-spin" />
                ) : null}
                {mode === 'reset-password' ? 'Send Reset Link' : mode === 'sign-in' ? 'Sign In' : 'Create Account'}
              </button>
            </form>

            <div className="mt-6 pt-4 border-t border-border text-center">
              {mode === 'sign-in' && (
                <Link
                  to="/reset-password"
                  className="text-sm text-muted-foreground hover:text-primary transition-colors block mb-2"
                >
                  Forgot password?
                </Link>
              )}
              <p className="text-muted-foreground text-sm">
                {mode === 'reset-password' ? (
                  <>
                    Remember your password?{' '}
                    <Link to="/sign-in" className="text-primary hover:underline font-medium">
                      Sign in
                    </Link>
                  </>
                ) : mode === 'sign-in' ? (
                  <>
                    Don't have an account?{' '}
                    <Link to="/sign-up" className="text-primary hover:underline font-medium">
                      Sign up
                    </Link>
                  </>
                ) : (
                  <>
                    Already have an account?{' '}
                    <Link to="/sign-in" className="text-primary hover:underline font-medium">
                      Sign in
                    </Link>
                  </>
                )}
              </p>
            </div>
          </div>
        </motion.div>
      </div>
    </div>
  )
}
